#create a variable x and give it a value
x=15
"""process of finding if the number is even or not
even numbers when divided by 2 the answer should be zero"""

result=x%2

#if statement, if the result is equal to 0 then its even else its odd

if result==0:
    print(f" The number {x} is an even number ")
else:
    print(f" The number {x} is an odd number")